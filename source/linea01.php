			<p class="project-desc">En 1973, nada más finalizar los estudios de la Licenciatura en Medicina en la Universidad de Salamanca, el Dr. José Luis Calvo se trasladó a la Cátedra de Histología de la Universidad de Madrid (Cátedra que finalmente obtendría, por oposición, unos 25 años después). En esta Cátedra, por la que han pasado insignes neurohistólogos españoles como Ramón y Cajal, Tello, Castro, Gallego, y tantos otros, entró en contacto con la cuna de la Escuela Neurohistológica Española. Además de desarrollar un interés por todo lo referido a la estructura del sistema nervioso, esto le permitió conocer, practicar y dominar las técnicas argénticas, muchas de ellas inventadas por histólogos de esta Cátedra. Aun sin restar importancia, de ningún modo, a las técnicas morfológicas modernas (histoquímica, inmunohistoquímica, marcadores específicos, sondas moleculares, etc.), nunca debe olvidarse el valor de las técnicas argénticas, tan injustamente olvidadas en la actualidad. En efecto, además de la belleza plástica de las imágenes que proporcionan las técnicas neurohistológicas clásicas, una gran parte del cuerpo de doctrina de la neurohistología se basa en ellas. Como aplicación inmediata de su aprendizaje de las técnicas argénticas, realizó un amplio estudio aplicando dichas técnicas a muestras de cerebros humanos provenientes de autopsias, tanto de sujetos de edad avanzada fallecidos por una enfermedad no neurológica, como de pacientes de patologías psiquiátricas. Los resultados demostraron la presencia de alteraciones, tanto en neuronas como en astrocitos y vasos sanguíneos, dando lugar a una serie de 5 publicaciones que se relacionan en este grupo.</p>
			
