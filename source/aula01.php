			<p class="project-desc">La reciente implantación en nuestra Facultad del nuevo plan de estudios para el Grado de Medicina adaptado al Espacio Europeo de Educación Superior ha supuesto, entre otros muchos cambios, un fuerte estímulo de la enseñanza práctica, con especial énfasis en implantar métodos y tecnologías que posibiliten y faciliten el autoaprendizaje. En nuestras asignaturas, una gran parte de la enseñanza práctica consiste en la observación al microscopio de preparaciones histológicas, con el fin de que el alumno pueda observar por sí mismo las estructuras microscópicas descritas en las clases teóricas. El problema es que las limitaciones impuestas por la escasez de salas de prácticas dotadas de microscopios, preparaciones y personal, junto a la abundancia de grupos prácticos y de actividades prácticas por alumno, no permiten que el alumno dirija el ritmo de su enseñanza práctica, debiendo limitarse a seguir el plan de clases prácticas impuesto por el Departamento. Otra demanda habitual de los alumnos es la posibilidad de disponer de todo el material práctico a lo largo del curso y, particularmente, en la vecindad de los exámenes.</p>			
			<p class="project-desc">Todas las consideraciones anteriores nos llevaron a plantear la posibilidad de implementar un sistema que simulase las prácticas microscópicas. Esto permitiría a los alumnos planificar la enseñanza práctica a su propio ritmo, además de proporcionar un acceso permanente al material práctico, sin las limitaciones de horario o de salas de prácticas convencionales. En nuestra idea, el sistema debería imitar en lo posible la observación microscópica, ofreciendo varias campos y varios aumentos de cada preparación, evitando la foto única por caracteristicas propia de la mayoría de los atlas. Por último, nos decidimos por la implementación del sistema en una aplicación informática, dada la calidad de imagen que puede mostrarse en un ordenador, así como la capacidad de reunir un alto número de imágenes en una sola aplicación. Hemos denominado a nuestro sistema <strong>Aula Virtual de Prácticas</strong> ya que, con él pretendemos imitar en lo posible la observación de preparaciones histológicas al microscopio. En el 2010, salió la primera parte de este sistema, un CD titulado <strong>Prácticas Virtuales de Organografía Microscópica Humana</strong> conteniendo más de 650 imágenes obtenidas de alrededor de 120 preparaciones histológicas que abarcan todos los órganos, aparatos y sistemas del cuerpo humano.</p>			
			<p class="project-desc">La acogida del CD ha sido excepcional, obligando a la editorial responsable del mismo (la Editorial Complutense) a la emisión de varias ediciones. Este CD ha sido adquirido, lógicamente, por alumnos de Medicina que los utilizan como complemento, material de apoyo y de repaso de sus clases prácticas. Pero también por alumnos de otras Facultades el grupo de Ciencias de la Salud, tanto de nuestra Universidad como de otras Universidades, que han visto en él un útil material complementario para la Organografía Microscópica. Y, por último, nos han llegado testimonios del uso de nuestro CD en Facultades en las que, debido a limitaciones presupuestarias o a ser de nueva creación, no disponen de un sistema lo suficientemente completo de aulas de prácticas dotadas de microscopios y de una histoteca completa. En estos casos, nuestro CD está siendo utilizado como parte de la enseñanza práctica y no sólo como un material de apoyo.</p>			
		
