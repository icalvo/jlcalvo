			<p class="project-desc">Este grupo de estudios, realizado, como el anterior, poco después de finalizar su Licenciatura en Medicina, tuvo para el Dr. José Luis Calvo el interés de ser la primera investigación financiada por medios externos (los Laboratorios Farmacéuticos Geigy) que obtuvo en su carrera, algo que en 1973 no era tan habitual como en la actualidad. La base de estos estudios debe buscarse también en los trabajos de la Escuela Neurohistológica española, concretamente las excelentes investigaciones de Santiago Ramón y Cajal sobre los fenómenos degenerativos y regenerativos de los nervios. Cajal ya había apuntado en sus estudios que la cicatriz formada tras las lesiones de los nervios dificultaba o impedía su correcta regeneración, generando lo que en anatomía patológica se describe como neuromas de amputación. El equipo del Dr. Calvo se planteó aprovechar los efectos anticicatriciales de los corticoides para comprobar si tenían efectos favorables en la regeneración del nervio periférico.</p>
			<p class="project-desc">Los resultados demostraron un notable efecto beneficioso tanto de los corticoides como de los antiinflamatorios no hormonales sobre la regeneración del nervio ciático lesionado de rata. El efecto era mucho más evidente en las lesiones por sección, en las que se forma una cicatriz, que en la compresión, donde los fenómenos cicatriciales están muy reducidos. Los resultados obtenidos con la oxifenilbutazona fueron el objeto de una extensa monografía editada por los Laboratorios Geigy. Años más tarde, como continuación de las investigaciones de un doctorando, el Dr. Calvo y su equipo completaron el estudio, abordando la caracterización del patrón enzimático de los macrófagos que aparecen en el nervio periférico lesionado por compresión, pudiendo determinar que su origen es principalmente hematógeno, con poca participación de las células de Schwann locales.</p>			

