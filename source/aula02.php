			<p class="project-desc">Con la experiencia obtenida en la primera entrega de nuestro proyecto <strong>Aula Virtual</strong>, acometimos la segunda parte del mismo, ahora dedicada a la Histología, es decir, a los tejidos. Éste es un contenido del existe poca iconografía en los libros de texto y atlas publicados, en los que los tejidos son sólo una parte de toda la estructura microscópica del organismo descrita en ellos. Y, muchas veces, las imágenes existentes son sólo esquemas simples, muy poco realistas. Por tanto, esta nueva parte de nuestro proyecto debería poseer un número elevado de imágenes microscópicas, no simples esquemas. Además, aunque el sistema empleado en la primera entrega (varias imágenes de distintos campos y a distintos aumentos) era un buen acercamiento a la observación al microscopio de una preparación, deseábamos una aproximación mejor, lo más parecida al manejo real del microscopio óptico. Por último, sin olvidar nuestro agradecimiento a la Editorial Complutense por la atención recibida en la primera entrega, nos planteamos contactar con otra editorial, en un intento de aumentar la difusión de nuestro proyecto.</p>
			<p class="project-desc">El resultado de esta nueva fase, las <strong>Prácticas Virtuales de Histología: Tejidos</strong>, será comercializado en 2012 por la Editorial Elsevier, una de las editoriales científicas más importantes dentro de nuestro área de conocimiento. El material formará parte de una novedosa plataforma online de contenido multimedia denominada "ElSaber" que va a ser lanzada por Elsevier para proveer de contenido multimedia a Universidades de España e Hispanoamérica. Constituye probablemente una de las mayores colecciones de imágenes de microscopía óptica específicas de los tejidos publicadas hasta ahora (casi 3 Gb de imágenes de microscopía óptica de los tejidos humanos). Como en el caso del proyecto anterior, cada imagen va acompañada de textos señalando las estructuras más importantes. Como novedad, en esta nueva entrega dedicada a los tejidos, hemos incorporado un sistema que permite cambiar el aumento y desplazarse por las imágenes (ahora de tamaño mucho mayor) de un modo muy similar al manejo de un microscopio óptico cuando se observa una preparacion en el mismo. Un ejemplo del sistema, puede verse <a href="/ftp/aula/mo.htm" target="_blank">aquí</a>. </p>			
		
