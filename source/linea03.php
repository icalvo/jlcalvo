			<p class="project-desc">En 1976, el Dr. José Luis Calvo obtuvo el grado de Doctor por la Universidad Complutense de Madrid, con la calificación de Sobresaliente Cum Laude. Como tema de Tesis Doctoral, elegió el desarrollo embrionario y maduración postnatal de la glándula pineal de pollo. La glándula pineal de las aves ocupa una posición intermedia entre las pineales sensoriales (ojo parietal), propias de reptiles y anfibios, y las pineales endocrinas, propias de los mamíferos. Utilizando el enfoque evolutivo, que tan buenos resultados ha proporcionado en las investigaciones biológicas, pensó que un buen conocimiento de la pineal de las aves proporcionaria una base sólida para afrontar con más seguridad el estudio de la pineal en los mamíferos.</p>
			<p class="project-desc">Como resultados de sus investigaciones en la pineal de pollo, publicó 8 trabajos en revistas de amplia difusión y factor de impacto. En ellos se detalla el desarrollo embrionario y evolución posteclosión de la glándula pineal, utilizando microscopía óptica, microscopía electrónica, histoquímica y técnicas argénticas. En conjunto, proporcionan la que, probablemente, es la descripción más completa de la glándula pineal en esta especie. Allí descubrió la presencia de restos de fotorreceptores, sometidos, como en la retina, a un proceso de renovación por digestión lisosómica. Describió la presencia de neuronas, la distribución de la inervación, la formación de rosetas (que aparecen en varias especies de mamíferos), los lisosomas de los pinealocitos, identificados con histoquímica al <acronym title="Microscopio Óptico">M/O</acronym> y <acronym title="Microscopio Electrónico">M/E</acronym>, la involución con la edad, etc. Incluso pudo demostrar que la administración de hormonas sexuales, que provoca un adelantamiento de la expresión de caracteres sexuales secundarios en el pollo, se acompaña de una maduración acelerada en la estructura pineal.</p>

