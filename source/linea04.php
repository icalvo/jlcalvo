			<p class="project-desc">Las células de microglia han sido, desde su descripción en 1919 por Rio-Hortega, el tipo celular más discutido del Sistema Nervioso Central. El equipo de investigación del Prof. Calvo se planteó la identificación de la microglia utilizando varios métodos de aproximación al problema. Así, estudiaron la aparición de la microglia en el periodo embrionario tardío y postnatal temprano, utilizando la microscopía electrónica y también técnicas histoquímicas específicas de macrófagos. Abordaron la caracterización de la microglia con métodos diferentes a los que se empleaban en el momento (sobre todo, técnicas argénticas). Así, en la herida cerebral, determinaron, utilizando técnicas histoquímicas, el patrón enzimático de los macrófagos cerebrales. Fue uno de los primeros grupos de investigación en emplear las lectinas para identificar la microglia, tanto adulta como en desarrollo, utilizando para ello varias especies de mamíferos. Hicieron una de las primeras demostraciones al microscopio electrónico de la positividad para lectinas de la microglia. Son también destacables sus estudios sobre la microglia en desarrollo en la retina, en la que la ausencia de meninges y vasos (en las capas internas) permite seguir con facilidad la aparición de estas células.</p>

