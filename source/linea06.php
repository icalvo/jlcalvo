			<p class="project-desc">La glándula pineal o epífisis es, sin duda, el componente menos conocido del sistema endocrino. Prácticamente todos los estudios morfológicos existentes sobre la glándula pineal en animales de experimentación se han realizado en la rata. Apenas si existen datos sobre la estructura pineal en otros mamíferos diferentes a la rata. El análisis de la morfología comparada es un excelente medio de abordaje para el estudio de estructuras poco conocidas. El Dr. José Luis Calvo y su equipo han investigado la estructura, ultraestructura e inmunohistoquímica de la glándula pineal en tres especies de mamíferos: gato, perro y conejo.</p>
			<p class="project-desc">Los resultados obtenidos demuestran que la glándula pineal de estos mamíferos tiene muchas semejanzas con la pineal humana. Así, se puede citar la presencia de verdaderos astrocitos en el paréquima pineal, la casi constante aparición de pigmento (con una ultraestructura muy peculiar, como demostraron por primera vez). Son también muy llamativas, entre otras, las modificaciones en la ultraestructura centriolar o la presencia de barras sinápticas. En resumen, estos estudios han aclarado la morfología de la glándula pineal, tan poco estudiada en estas especies.</p>

