			<p class="project-desc">Con la experiencia acumulada a través de todas sus investigaciones sobre la morfología de la glándula pineal, descritas en cerca de 40 trabajos publicados en revistas científicas de primera línea, el Prof. Calvo y su equipo se planteó contribuir al conocimiento sobre las actividades funcionales de esta glándula. Para ello, estudiaron las modificaciones morfológicas de la glándula pineal de rata ante una variedad de situaciones, tanto fisiológicas como provocadas experimentalmente.</p>
			<p class="project-desc">Así, dentro de las situaciones fisiológicas investigaron la respuesta pineal al ciclo día-noche, determinando los cambios en el ritmo mitótico de los pinealocitos en la fase de luz y de oscuridad. También han determinado las modificaciones en la población celular pineal según el sexo de la rata. Por último, estudiaron las modificaciones que aparecen en la glándula en el envejecimiento. En cuanto a las modificaciones experimentales estudiadas por el equipo, se han centrado en el efecto de la denervación simpática, tanto quirúrgica como farmacológica, sobre el parénquima pineal.</p>

