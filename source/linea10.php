			<p class="project-desc">En esta línea, el Dr. José Luis Calvo y su equipo han demostrado la coexpresión de dos proteínas de filamentos intermedios, la proteína glial fibrilar ácida (GFAP) y la vimentina (VIM) en varios tipos de astrocitos normales y patológicos. Esto lo han podido conseguir gracias a una técnica muy simple, novedosa, sencilla y económica que, de hecho, mereció ser publicada nada menos que en Brain Research, una de las revistas más importantes en investigaciones neurológicas. En dicha técnica, se realiza la inmunotinción para GFAP y VIM en dos cortes semifinos de 0'5 μm consecutivos de material incluido en Epon. Por supuesto, las técnicas inmunohistoquímicas deben adaptarse para su uso en cortes incluidos en plástico. El escaso grosor de los cortes semifinos (0'5 μm) permite observar las mismas células en dos cortes consecutivos, permitiendo una fácil demostración de la coexpresión antigénica. Hasta la publicación de este método, la coexpresión no podía demostrarse, de forma inequívoca, por métodos morfológicos. Gracias a esta técnica pudieron demostrar la coexpresión de GFAP y VIM en astrocitos cerebrales, del nervio óptico, así como en los astrocitos reactivos que aparecen tras una lesión traumática del SNC. En este último caso, esa investigación mostró, por primera vez, no sólo la coexpresión, sino también la inmunoreactividad para VIM, hasta entonces no demostrada en los astrocitos reactivos. También, en reconocimiento a las investigaciones de nuestro grupo, Jean de Vellis, de la Universidad de California (UCLA), encargó al equipo el capítulo sobre la reacción astroglial tras la herida cerebral de su magnífico y conocido libro: "Neuroglia in the Aging Brain".</p>

