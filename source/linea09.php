			<p class="project-desc">En esta línea, el Prof. Calvo y su equipo han caracterizado, con una gran variedad de métodos morfológicos, el segundo tipo celular de la glándula pineal de la rata. Aunque inicialmente dieron a estas células el nombre de pinealocitos tipo II, sus estudios posteriores demostraron de manera evidente, la naturaleza glial (emparentada con los astrocitos) de estas células, por lo que propusieron denominarlas como células gliales. Esta línea es probablemente la más importante dentro de los estudios del equipo sobre la glándula pineal de rata. Los resultados obtenidos en la misma línea han sido publicados en revistas de tanto prestigio como Brain Research, Anatomical Record o Journal of Pineal Research, todas ellas con un factor de impacto muy elevado.</p>
			<p class="project-desc">Los estudios de esta línea mostraron, por primera vez en la investigación sobre la glándula pineal, la presencia de antígenos propios de astrocitos como vimentina, proteína <acronym title="Glial Fibrilar Ácida">GFA</acronym> y S-100 en las células gliales de la glándula pineal de rata, demostrando además que dichos antígenos se coexpresan, tanto al <acronym title="Microscopio Óptico">M/O</acronym> como al <acronym title="Microscopio Electrónico">M/E</acronym>, en el segundo tipo celular pineal. La presencia de antígenos gliales en células de la glándula pineal no es de extrañar, dado el origen neuroectodérmico de la glándula a partir del techo del III ventrículo craneal. Pero, hasta entonces, nadie había demostrado dichos antígenos, su coexpresión o los cambios que muestran a lo largo del desarrollo pineal. Además, el equipo consiguió demostrar la presencia de otras moléculas importantes como el factor de crecimiento nervioso (NGF) y el factor de crecimiento fibroblástico (bFGF) en las células gliales de la pineal de rata. </p>

